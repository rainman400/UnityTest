package com.unity.game.resources;


public class ImageRsrc {

	private Integer id;
	private String url;
	private Integer type;

	public Integer getId() {
		return id;
	}

	public void setId(Integer imageId) {
		this.id = imageId;
	}

	public String getUrl() {
		return url;
	}
	public void setUrl(String imageUrl) {
		this.url = imageUrl;
	}

	public Integer getType() {
		return type;
	}

	public void setType(Integer imageType) {
		this.type = imageType;
	}

}
